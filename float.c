//divsion and mod operations on Integer , float values.....
#include <stdio.h>
	int main()
 {
 		int x = 4/9;
 		printf("%d\n",x);
		int y = 4/9;
 		printf("%.2f\n",y);


		float a = 4/9;
 		printf("%.2f\n",a);
 		float b = 4/9;
 		printf("%d\n",b);


 		float c = 4.0/9.0;
 		printf("%.2f\n",c);
 		float d = 4.0/9.0;
 		printf("%f\n",d);

		return 0;
 }
