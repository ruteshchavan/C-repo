// octal value and hexa decimal value.....
#include <stdio.h>
  int main()
	{
	  int x=033; // 0 presents octal
	  int y=0x43FF; // 0x represnts hexadecmal
	  printf("%d\n",x); // conversion of octal to decimal
	  printf("%d\n",y); // conversion of hexadecimal to decimal
	  printf("%x\n",y); // display hexadecimal value 
	  printf("%o\n",x); // display octal value
	  return 0;
	}	
	